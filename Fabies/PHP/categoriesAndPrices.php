    <h2>Категория “А”</h2>

    <p> Категория “А” дава право за управление на  мотоциклети и триколесни моторни превозни средства
        с мощност над 15 kW /без ограничения в кубатурата/.</p>

    <h4>Курсът за категория “А” включва теоретична и практическа подготовка:</h4>

    <ul class="courseInfo">
        <li>Теоретично обучение:</li>
        <ul>
            <li>при придобита правоспособност от категория В, се преминава към теоретично обучение,
                което включва 4 учебни часа „Общо устройство на мотоциклета”;</li>
            <li>за лица, които не притежават  правоспособност от категория “В” – теоретичното обучение е в размер на 36 часа
                „Безопасност на движението” и 4 часа „Общо устройство на мотоциклета</li>
        </ul>
        <li>Практическо обучение:</li>
        <ul>
            <li>Практическото обучение включва 20 учебни часа управление на мотоциклет.
                Обучението се провежда на полигон и управление в градски условия градски условия.
                Обучението на полигон представлява изпълнение на 5 елемента /вижте видеото/.</li>
        </ul>
    </ul>

    <h3>Изпит за категория “А”:</h3>

    <ul class="courseInfo">
        <li>Изпита за категория “А” се състои от 2 части:</li>
            <ul>
                <li> Полагане на официален теоретичен изпит пред ДАИ, за кандидати, които не притежават категория “В”.</li>
                <li> Полагане на практически изпит на полигон и управление в градски условия.
                    Кандидатите, които притежават категоря В се явяват само на практически изпит.</li>
            </ul>
    </ul>

    <h2 class="documents">Необходими документи:</h2>

    <ol>
        <li>Копие на лична карта</li>
        <li>Копие на диплома</li>
        <li>Свидетелство за управление на МПС минимум категория “В” /ако притежават такова/</li>
        <li>Снимка</li>
    </ol>

