    <h2>Категория “В”</h2>

    <p>Шофьорската книжка за категория “В” позволява управление на леки, лекотоварни и товарни автомобили с ръчна
        скоростна кутия и с тегло включително и товара до 3,5 тона и до 9 места.</p>

    <p>Курсът включва 40 часа теория, разпределени в рамките на две седмици и 31 часа кормуване. Продължителността на учебния час при практическо обучение е 50 мин. След първата седмица от теорията можете да започнете кормуването.
        Графикът се прави с инструктора в зависимост от вашите възможности и желания.</p>

    <ul class="courseInfo">
        <li>Теоретично обучение:</li>
            <ul>
                <li>Модул Общо устройство на автомобила</li>
                <li>Научаване на правилата за движение</li>
                <li>Модул Безопасност на движението</li>
            </ul>
        <li>Практическо обучение:</li>
            <ul>
                <li>Придобиване на навици за работа с уредите за управление на автомобила</li>
                <li>Обучение на полигон за потегляне, спиране, маневри и паркиране</li>
                <li>Научаване и изпълнение на правилата за движение по пътищата</li>
                <li>Реални навици на управление на автомобил при определени условия:</li>
                    <ul>
                        <li>при интензивно градско движение</li>
                        <li>извънградски условия</li>
                        <li>полигон и автомагистрала</li>
                    </ul>
            </ul>
    </ul>

    <h3>Изпит за категория “В”:</h3>

    <ul class="courseInfo">
        <li>Теоретичният изпит е писмен. Решава се една изпитна листовка с 45
            въпроса с разрешени не повече от десет наказателни точки</li>
        <li>Практическият изпит се състои от управление на МПС от категория „В“ в населено място минимум 25 минути</li>
    </ul>

    <h2 class="documents">Необходими документи:</h2>
    <ol>
        <li>Диплома за завършено основно, средно или висше образование</li>
        <li>Лична карта</li>
        <li>Две паспортни снимки</li>
    </ol>

